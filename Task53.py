mark = float(input())
if mark >= 4.0:
    print('A+')
if mark == 4.0:
    print('A')
if mark == 3.7:
    print('A-')
if mark == 3.3:
    print('B+')
if mark == 3.0:
    print('B')
if mark == 2.7:
    print('B-')
if mark == 2.3:
    print('C+')
if mark == 2.0:
    print('C')
if mark == 1.7:
    print('C-')
if mark == 1.3:
    print('D+')
if mark == 1.0:
    print('D')
if mark == 0:
    print('D-')
