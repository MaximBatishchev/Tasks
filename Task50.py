m = float(input())
if m < 2:
    print("Минимальное")
if  2 <= m < 3:
    print("Очень слабое")
if  3 <= m < 4:
    print("Слабое")
if  4 <= m < 5:
    print("Промежуточное")
if  5 <= m < 6:
    print("Умеренное")
if  6 <= m < 7:
    print("Сильное")
if  7 <= m < 8:
    print("Очень сильное")
if  8 <= m < 10:
    print("Огромное")
if  m >= 10:
    print("Разрушительное")
