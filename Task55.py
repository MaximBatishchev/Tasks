l = int(input())
if 380 <= l < 450:
    print('Фиолетовый')
elif 450 <= l < 495:
    print('Синий')
elif 495 <= l < 570:
    print('Зеленый')
elif 570 <= l < 590:
    print('Желтый')
elif 590 <= l < 620:
    print('Оранжевый')
elif 620 <= l <= 750:
    print('Красный')
else:
    print('Вне диапозона')
